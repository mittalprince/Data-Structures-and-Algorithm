int minimumCost(vector<int>& cost) {
    sort(cost.begin(), cost.end(), greater<>());
    int ans=0, n=cost.size(), cnt=0, i=0;
    while(i<n){
        if(cnt<2){
            ans += cost[i];
            cnt++;
        } else cnt=0;
        i++;
    }
    return ans;
}

