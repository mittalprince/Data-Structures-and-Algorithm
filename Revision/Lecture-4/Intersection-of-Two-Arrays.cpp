vector<int> intersection(vector<int>& nums1, vector<int>& nums2) {
    map<int, bool> m;
    for(auto i: nums1){
        m[i]=true;
    }
    vector<int>ans;
    for(auto i: nums2){
        if(m[i]) {
            ans.push_back(i);
            m[i]=false;
        }
    }
    return ans;
}

