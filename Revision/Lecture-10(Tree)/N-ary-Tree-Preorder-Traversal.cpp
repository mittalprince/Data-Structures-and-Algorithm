class Node{
public:
    int val;
    vector<Node*> children;

    Node() {}

    Node(int _val) {
        val = _val;
    }

    Node(int _val, vector<Node*> _children) {
        val = _val;
        children = _children;
    }
};

void solve(Node *root, vector<int>&ans){
    if(!root)return;

    ans.push_back(root->val);
    for(Node *child: root->children){
        solve(child, ans);
    }

}

vector<int> preorder(Node* root) {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    if(root==NULL)return {};

    vector<int>ans;
    solve(root, ans);
    return ans;
}

