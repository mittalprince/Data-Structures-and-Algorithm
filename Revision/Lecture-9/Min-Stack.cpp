class MinStack {
public:
    stack<pair<int, int> >s;
    /** initialize your data structure here. */
    MinStack() {
        ios_base::sync_with_stdio(false);
        cin.tie(NULL);
    }
    
    void push(int x) {
        int m;
        if(s.empty())m=x;
        else{
            m=min(x, s.top().second);
        }
        s.push({x, m});
    }
    
    void pop() {
        if(s.empty())return ;
        s.pop();
    }
    
    int top() {
        return s.top().first;
    }
    
    int getMin() {
        return s.top().second;
    }
};

