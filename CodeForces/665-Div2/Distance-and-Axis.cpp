void solve(){
    ll n,k;
    cin>>n>>k;
    ll ans=0;
    if(n>k) {
        if((n%2==0 && k%2==0)||(n%2!=0 && k%2!=0))
            ans=0;
        else
            ans=1;
    }
    else{
        ans=abs(n-k);
    }
    cout<<ans<<endl;
}

